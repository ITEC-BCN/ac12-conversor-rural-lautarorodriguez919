function menu () {
    opcio_usuari = game.askForString("Quieres intercambiar objetos? si o no", 2)
    lista_de_texto = ["si", "no"]
    valor = lista_de_texto.indexOf(opcio_usuari)
    opcion = lista_de_texto[valor]
    if (opcio_usuari == opcion) {
        if (opcio_usuari == "si") {
            escenario_menu(true, 0)
        }
        if (opcio_usuari == "no") {
            crear_escenario(true)
        }
    } else {
        tiles.setCurrentTilemap(tilemap`nivel12`)
        game.showLongText("ERROR opción no valida, porfavor indica \"si\" o \"no\"", DialogLayout.Bottom)
        menu()
    }
}
